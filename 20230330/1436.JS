const input = require("fs")
  .readFileSync("20230330/example2.txt")
  .toString()
  .trim()
  .split("\n");

let num = Number(input[0]);
let count = 0;
let growingNum = 666;

while (true) {
  if (String(growingNum).includes(666)) count++;

  if (count === num) {
    console.log(growingNum);
    break;
  }
  growingNum++;
}

